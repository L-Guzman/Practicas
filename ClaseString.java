
public class ClaseString {
	public static void main(String args[]){
	
		
		String prueba="APRENDIENDO EN JAVA";
		System.out.println(prueba);
		System.out.println("El texto tiene " + prueba.length() + " letras");
		System.out.println("La frase comienza con la letra: " + prueba.charAt(0));
		System.out.println("La frase termina con la letra: " + prueba.charAt(prueba.length() - 1));
		System.out.println("La letra en la posición 9 es: " + prueba.charAt(9));
		
	}

}
